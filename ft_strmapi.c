#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	size_t	lens;
	char	*dst;

	i = 0;
	if (s == NULL)
		return (NULL);
	lens = ft_strlen(s);
	dst = (char *)malloc((lens + 1) * sizeof(char));
	if (dst == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		dst[i] = f(i, s[i]);
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
